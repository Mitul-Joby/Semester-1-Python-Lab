'''PROGRAM TO CONVERT TEMPERATURES IN CELSIUS TO FAHRENHEIT'''

#Input for temperature in Celsius
TempC = float(input('\nEnter temperature in Celsius :'))

#Calculating temperature in Fahrenheit with formula
TempF = ( TempC * (9/5) ) + 32

#Displaying the output
print(TempC,'degree Celsius is equal to',TempF,'degree Fahrenheit')